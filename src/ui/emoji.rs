pub(crate) fn find_matches(prefix: &str) -> Vec<String> {
    let lower = prefix.to_lowercase();
    EMOJI.iter()
        .filter(|(name, _)| name.starts_with(lower.as_str()))
        .map(|(_, ch)| ch.to_string())
        .collect()
}
